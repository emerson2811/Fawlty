numbers = [0, 100, 200, 300, 400, 500, 600]

print(numbers[2:5])
#prints out values from the 2nd location in the list to the 5th position in the list

print(numbers[:3])
#this will give you all the values before index position 3

print(numbers[2:])
#prints values from the second index position onwards

print(numbers[1:6:2])
#prints values from index position 1 to 6 BUT prints every two values